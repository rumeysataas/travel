class UserState {
  String? _email;
  set setEmail(String? value) => this._email = value;
  get email => this._email;

  String? _password;
  set setPassword(String? value) => this._password = value;
  get password => this._password;

  String? _name;
  set setName(String? value) => this._name = value;
  get name => this._name;
}
